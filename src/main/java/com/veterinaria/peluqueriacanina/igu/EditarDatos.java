    private void cargarDatos(int id) {
        this.mascota = control.traerMascota(id);
        
        this.txtCelDuenio.setText(mascota.getUnDuenio().getCelDuenio());
        this.txtColor.setText(mascota.getColor());
        this.txtNombreDuenio.setText(mascota.getUnDuenio().getNombreDuenio());
        this.txtNombreMascota.setText(mascota.getNombreMascota());
        this.txtObservaciones.setText(mascota.getObservaciones());
        this.txtRaza.setText(mascota.getRaza());
        
        if(mascota.getAlergico().equals("si")){        
            this.cmbAlergico.setSelectedIndex(1);
        } else {
            this.cmbAlergico.setSelectedIndex(2);
        }
        
        if(mascota.getAtencionEspecial().equals("si")){        
            this.cmbAtEspecial.setSelectedIndex(1);
        } else {
            this.cmbAtEspecial.setSelectedIndex(2);
        }
        
    }
